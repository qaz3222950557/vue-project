<template>
    <!-- svg:图标外层容器节点，内部需要与use标签使用 -->
    <svg :style="{width,height}">
        <!-- xlink:href执行用哪一个图标，属性值务必使用#icon-图标名字 -->
        <!-- use标签fill属性可以设置图标的颜色  -->
        <!-- 使用来自父组件传来的数据 -->
        <use :xlink:href="prefix+name" :fill="color"></use>
        <!-- 不使用父组件传来的数据 -->
        <!-- <use xlink:href="#icon-first" fill="red"></use> -->
    </svg>
</template>
<script lang="ts" setup name="test1">
    defineProps({
        //属性值前缀
        prefix:{
            type:String,
            default:'#icon-'
        },
        //接收来自父组件提供的图标名字
        name:String,
        //接收来自父组件提供的颜色
        color:{
            type:String,
            default:'pink'
        },
        width:{
            type:String,
            default:'16px'
        },
        height:{
            type:String,
            default:'16px'
        }
    })
</script>

